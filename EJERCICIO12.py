# Este programa imprime un dibujo en forma de pirámide con asteriscos utilizando bucles 'for'.

# Parte superior del dibujo
for i in range(1, 6):  # Imprime las filas ascendentes
    print('*' * i)

# Parte inferior del dibujo
for i in range(4, 0, -1):  # Imprime las filas descendentes
    print('*' * i)

