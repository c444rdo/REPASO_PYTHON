# Este programa imprime todos los elementos de una lista utilizando un bucle 'while'.
# El bucle continúa hasta que se recorre toda la lista.

frutas = ["manzana", "plátano", "cereza", "uva"]
i = 0  # Inicializamos el índice
while i < len(frutas):  # El bucle sigue hasta recorrer toda la lista
    print(frutas[i])  # Imprime el elemento en el índice 'i'
    i += 1  # Incrementamos el índice

